    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed
        // TODO add your handling code here:
        String nombre = userName.getText();
        String contraseña = password.getText();
        String confirmarContraseña = confirmPassword.getText();
        
        if (!nombre.isEmpty() && !contraseña.isEmpty() && !confirmarContraseña.isEmpty()) {
            
            if (contraseña.equals(confirmarContraseña)) {
                
                try {
                    usuarioController.crearUsuario(nombre, contraseña);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
                }
                
            }else{
                System.out.println("Las contraseñas deben ser iguales");
            }
            
        } else 
            System.out.println("Los campos son obligatorios");
    }//GEN-LAST:event_registrarActionPerformed

